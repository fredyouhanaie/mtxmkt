-module(mtxmkt).

%% API exports
-export([mm_openread/1]).


%%====================================================================
%% API functions
%%====================================================================


%%--------------------------------------------------------------------
%% @doc open a file for reading
%% @spec
%% @end
%%--------------------------------------------------------------------

mm_openread(Filename) ->
    case file:open(Filename, [read]) of
	{ok, IOdev} ->
	    IOdev;
	{error, Reason} ->
	    {error, Reason, "Could not open the file"}
    end.


